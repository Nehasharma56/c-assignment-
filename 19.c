#include<stdio.h>
int main ()
{
    int num1=20;
    int num2=15;

    printf("the original value of num1 is %d\n",num1);
    num1=++num1;
    printf("updated value of num1 is %d\n",num1);

    printf("the original value of num2 is %d\n",num2);
    num2= --num2;
    printf("updated value of num2 is %d\n",num2);

    printf("\n Neha sharma");

    return 0;
}
